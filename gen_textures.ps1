# ======================== ITEM ICONS (16x16) ========================
function Save-Icon($name, $data) {
    $bmp = New-Object System.Drawing.Bitmap(16, 16)
    for ($y = 0; $y -lt 16; $y++) {
        for ($x = 0; $x -lt 16; $x++) {
            $bmp.SetPixel($x, $y, $script:pal[$data[$y][$x]])
        }
    }
    $bmp.Save("$base\items\$name.png", [System.Drawing.Imaging.ImageFormat]::Png)
    $bmp.Dispose()
    Write-Output "$name icon saved"
}

