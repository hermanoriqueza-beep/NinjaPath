function Paint-Face($bmp, $ox, $oy, $data) {
    for ($y = 0; $y -lt $data.Count; $y++) {
        $row = $data[$y]
        for ($x = 0; $x -lt $row.Count; $x++) {
            $ci = $row[$x]
            $px = $ox + $x; $py = $oy + $y
            if ($px -ge 0 -and $px -lt $bmp.Width -and $py -ge 0 -and $py -lt $bmp.Height) {
                $bmp.SetPixel($px, $py, $script:pal[$ci])
            }
        }
    }
}

